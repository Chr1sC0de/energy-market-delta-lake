def emit(message: str, *, err: bool = False) -> None:
    stream = sys.stderr if err else sys.stdout
    stream.write(f"{message}\n")
    stream.flush()


def format_command(args: list[str] | tuple[str, ...]) -> str:
    return " ".join(shlex.quote(arg) for arg in args)


def parse_github_datetime(value: str) -> datetime:
    if value == "":
        return datetime.min.replace(tzinfo=UTC)
    return datetime.fromisoformat(value.replace("Z", "+00:00"))


def extract_login(value: Any) -> str | None:
    if isinstance(value, dict) and "login" in value:
        return str(value["login"])
    return None


def parse_comment_count(value: Any) -> int:
    if isinstance(value, list):
        return len(value)
    if value is None:
        return 0
    return int(value)


def parse_repo_slug(remote_url: str) -> str:
    remote_url = remote_url.strip()
    patterns = (
        r"^git@github\.com:(?P<slug>[^/]+/[^/]+?)(?:\.git)?$",
        r"^https://github\.com/(?P<slug>[^/]+/[^/]+?)(?:\.git)?/?$",
        r"^ssh://git@github\.com/(?P<slug>[^/]+/[^/]+?)(?:\.git)?/?$",
    )
    for pattern in patterns:
        match = re.match(pattern, remote_url)
        if match is not None:
            return match.group("slug")
    raise ValueError(f"Cannot derive GitHub repo from remote URL: {remote_url}")


def slugify(value: str, *, max_length: int = 56) -> str:
    slug = re.sub(r"[^a-z0-9]+", "-", value.lower()).strip("-")
    if slug == "":
        slug = "issue"
    if len(slug) <= max_length:
        return slug
    return slug[:max_length].rstrip("-")


def section_body(markdown: str, heading: str) -> str | None:
    pattern = re.compile(
        rf"(?ims)^#{{1,6}}\s+{re.escape(heading)}\s*$\n"
        rf"(?P<body>.*?)(?=^#{{1,6}}\s+\S|\Z)"
    )
    match = pattern.search(markdown)
    if match is None:
        return None
    return match.group("body").strip()


def missing_required_sections(markdown: str) -> list[str]:
    missing: list[str] = []
    for heading in REQUIRED_ISSUE_SECTIONS:
        body = section_body(markdown, heading)
        if body is None or body.strip() == "":
            missing.append(heading)
    return missing


def parse_blockers(markdown: str) -> list[int]:
    blocked_by = section_body(markdown, "Blocked by")
    if blocked_by is None:
        return []
    blockers = sorted({int(value) for value in re.findall(r"(?:#|/issues/)(\d+)", blocked_by)})
    return blockers


def issue_has_any_label(issue: Issue, labels: frozenset[str]) -> bool:
    return len(issue.labels.intersection(labels)) > 0


def is_ready_candidate(issue: Issue) -> bool:
    if READY_LABEL not in issue.labels:
        return False
    return not issue_has_any_label(issue, IMPLEMENTATION_STOP_LABELS)


def is_basic_triage_candidate(issue: Issue) -> bool:
    if issue_has_any_label(issue, TRIAGE_STOP_LABELS):
        return False
    state_labels = issue.labels.intersection(TRIAGE_STATE_LABELS)
    return not state_labels or NEEDS_TRIAGE_LABEL in state_labels


def root_prek_needed(changed_file: str) -> bool:
    return (
        changed_file in {"AGENTS.md", "README.md", ".pre-commit-config.yaml"}
        or changed_file.startswith("docs/")
        or changed_file.startswith("infrastructure/")
        or (
            changed_file.startswith("backend-services/")
            and not changed_file.startswith(AEMO_ETL_PREFIX)
        )
    )


def select_qa_commands(changed_files: list[str], repo_root: Path) -> list[QACommand]:
    commands: list[QACommand] = []
    has_aemo_etl_change = any(path.startswith(AEMO_ETL_PREFIX) for path in changed_files)
    if has_aemo_etl_change:
        aemo_root = repo_root / AEMO_ETL_PREFIX
        commands.extend(
            [
                QACommand(("make", "unit-test"), aemo_root, "aemo-etl Unit test"),
                QACommand(("make", "component-test"), aemo_root, "aemo-etl Component test"),
                QACommand(("make", "integration-test"), aemo_root, "aemo-etl Integration test"),
                QACommand(("make", "run-prek"), aemo_root, "aemo-etl Commit check"),
            ]
        )

    if any(root_prek_needed(path) for path in changed_files):
        commands.append(QACommand(("prek", "run", "-a"), repo_root, "root Commit check"))

    if any(path.startswith(("scripts/", "tests/")) for path in changed_files):
        commands.append(
            QACommand(
                ("python3", "-m", "unittest", "discover", "-s", "tests"),
                repo_root,
                "Ralph unit tests",
            )
        )

    deduped: list[QACommand] = []
    seen: set[tuple[tuple[str, ...], Path]] = set()
    for command in commands:
        key = (command.args, command.cwd)
        if key not in seen:
            deduped.append(command)
            seen.add(key)
    return deduped


def parse_git_status_paths(status_output: str) -> list[str]:
    paths: list[str] = []
    for line in status_output.splitlines():
        if len(line) < 4:
            continue
        path = line[3:].strip()
        if " -> " in path:
            path = path.rsplit(" -> ", 1)[1]
        paths.append(path.strip('"'))
    return sorted(set(paths))


def looks_like_environment_failure(error: CommandFailure) -> bool:
    output = f"{error.stdout}\n{error.stderr}".lower()
    return any(pattern in output for pattern in ENVIRONMENT_FAILURE_PATTERNS)


