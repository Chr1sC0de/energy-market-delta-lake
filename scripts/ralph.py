def section_body(markdown: str, heading: str) -> str | None:
    pattern = re.compile(
        rf"(?ims)^#{{1,6}}\s+{re.escape(heading)}\s*$\n"
        rf"(?P<body>.*?)(?=^#{{1,6}}\s+\S|\Z)"
    )
    match = pattern.search(markdown)
    if match is None:
        return None
    return match.group("body").strip()


def missing_required_sections(markdown: str) -> list[str]:
    missing: list[str] = []
    for heading in REQUIRED_ISSUE_SECTIONS:
        body = section_body(markdown, heading)
        if body is None or body.strip() == "":
            missing.append(heading)
    return missing


def parse_blockers(markdown: str) -> list[int]:
    blocked_by = section_body(markdown, "Blocked by")
    if blocked_by is None:
        return []
    blockers = sorted({int(value) for value in re.findall(r"(?:#|/issues/)(\d+)", blocked_by)})
    return blockers


