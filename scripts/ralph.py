    if not paths:
        return "\n".join(lines)

    preview = paths[:DIRTY_WORKTREE_STATUS_PREVIEW_LIMIT]
    lines.extend(["", "Changed paths:", *[f"- {path}" for path in preview]])
    remaining = len(paths) - len(preview)
    if remaining > 0:
        lines.append(f"- ... and {remaining} more")
    return "\n".join(lines)


