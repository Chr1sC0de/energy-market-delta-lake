class RalphError(RuntimeError):
    """Base error for user-facing loop failures."""


class CommandFailure(RalphError):
    """A subprocess command failed."""

    def __init__(
        self,
        command: list[str],
        cwd: Path,
        returncode: int,
        stdout: str,
        stderr: str,
        log_path: Path | None,
    ) -> None:
        super().__init__(f"Command failed ({returncode}): {format_command(command)}")
        self.command = command
        self.cwd = cwd
        self.returncode = returncode
        self.stdout = stdout
        self.stderr = stderr
        self.log_path = log_path


class IssueFailure(RalphError):
    """An issue-specific failure that should not stop the whole drain."""

    def __init__(self, message: str, log_path: Path | None = None) -> None:
        super().__init__(message)
        self.log_path = log_path


class EnvironmentFailure(IssueFailure):
    """A local environment failure that should stop the drain."""


